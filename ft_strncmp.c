#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (s1[i] < s2[1])
			return (-1);
		else if (s1[i] > s2[1])
			return (1);
		else
			i++;
	}
	return (0);
}
