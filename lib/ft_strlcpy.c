#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size == 0)
		return (ft_strlen(src));
	if (src != 0 && *src != 0)
	{
		while (i < size - 1)
		{
			if (src[i] == 0)
				break ;
			dst[i] = src[i];
			i++;
		}
	}
	dst[i] = 0;
	return (ft_strlen(src));
}
